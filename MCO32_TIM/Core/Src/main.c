	  /* Imprime valor do timer1 */
	  size = sprintf(mensagem, "TIM2 = %lu\r\n", htim1.Instance->CNT);
	  HAL_UART_Transmit(&huart2, (uint8_t*)mensagem, size, 100);
	  HAL_Delay(20);
#endif

#ifdef EX2
	  /* Ajusta dutycycle do pwm (CH4 - PB1) */
	  dutycycle = (dutycycle + 1) %101;
	  htim3.Instance->CCR4 = dutycycle;
#endif

#ifdef EX3
	  for(uint8_t i=0; i<=180; i=i+10)
	  {
		  htim2.Instance->CCR4 = i + 60;
		  HAL_Delay(200);
	  }
#endif

